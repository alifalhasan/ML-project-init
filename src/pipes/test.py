def add(x, y):
    """
    Performs addition.

    Parameters:
    - x: The first input number.
    - y: The second input number.

    Returns:
    int: The resulting answer after addition.

    Example:
    >>> x = 2
    >>> y = 3
    >>> add(x, y)
    5
    """
    return (x + y)

if __name__ == "__main__" :
    print(add(2, 3))
